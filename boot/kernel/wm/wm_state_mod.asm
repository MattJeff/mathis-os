; ============================================================================
; WM_STATE_MOD.ASM - Window Manager State
; ============================================================================
; Window array and state variables
; ============================================================================

[BITS 64]
[DEFAULT REL]

; ============================================================================
; CONSTANTS
; ============================================================================
WM_MAX_WINDOWS          equ 8
WIN_STRUCT_SIZE         equ 56

; ============================================================================
; EXPORTS
; ============================================================================
global wm_windows
global wm_window_count
global wm_active_index
global wm_drag_offset_x
global wm_drag_offset_y

; ============================================================================
; DATA
; ============================================================================
section .data

wm_window_count:        dd 0
wm_active_index:        dd -1
wm_drag_offset_x:       dd 0
wm_drag_offset_y:       dd 0

; ============================================================================
; BSS - Window array
; ============================================================================
section .bss

wm_windows:             resb WM_MAX_WINDOWS * WIN_STRUCT_SIZE
